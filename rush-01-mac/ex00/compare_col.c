#include <stdio.h>
void compare_col(int col1[4][6][4],int col2[4][6][4],int out_data[4][6][4])
{
	int i = 0;
	int j = 0;
	int k = 0;
	int temp = 0;

	while (i<4)
	{
		while (j<6)
		{
			while (k<4)
			{
				temp = j;
				j = 0;
				if (col1[i][temp][k] == col2[i][j][k])
				{
					out_data[i][temp][k] = col1[i][temp][k];
				}
				else if (col1[i][temp][k] == col2[i][j+1][k])
				{
					out_data[i][temp][k] = col1[i][temp][k];
				}
				else if (col1[i][temp][k] == col2[i][j+2][k])
				{
					out_data[i][temp][k] = col1[i][temp][k];
				}
				else if (col1[i][temp][k] == col2[i][j+3][k])
				{
					out_data[i][temp][k] = col1[i][temp][k];
				}
				else if (col1[i][temp][k] == col2[i][j+4][k])
				{
					out_data[i][temp][k] = col1[i][temp][k];	
				}
				else if (col1[i][temp][k] == col2[i][j+5][k])
				{
					out_data[i][temp][k] = col1[i][temp][k];		
				}
				else
				{
					j = temp;
					break;
				}
				k++;
				j = temp;
				
			}
			k = 0;
			j++;
		}
		j = 0;
		i++;
	}
}

