int	main(int argc, char **argv)
{
	while(*argv[1])
		if (*argv[1] > 'A' && *argv[1] < 'Z')
			*argv[1] += 32;
		else if (*argv[1] > 'a' && *argv[1] < 'z')
			*argv[1] -= 32;
		
		write(1,argv[1],1);
}